package day25;

public class Main {
    
    public static void main(String[] args) {
        //nilai awal variabel a dan b
        int a = 8;
        int b = 6;
        //nila a dan b setelah di increment dan dicrement
        a++;  //post increment
        System.out.println("nilai a di post increment: " + a);
        ++a;  //pre increment
        System.out.println("nilai a di pre increment: " + a);
        b--;  //post dicrement
        System.out.println("nilai b di post decrement: " + b);
        --b;  //pre dicrement
        System.out.println("nilai b di pre dicrement: " + b);
    }
    
}
